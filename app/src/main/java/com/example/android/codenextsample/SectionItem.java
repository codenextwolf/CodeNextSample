package com.example.android.codenextsample;

public class SectionItem {
    private int photo;
    private String title;

    public SectionItem(int photo, String title) {
        this.photo = photo;
        this.title = title;
    }

    public int getPhoto() {
        return photo;
    }

    public String getTitle() {
        return title;
    }
}
